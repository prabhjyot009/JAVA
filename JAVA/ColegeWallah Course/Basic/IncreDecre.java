public class IncreDecre {
    public static void main(String[] args) {
        int x = 5;
        System.out.println("Increment and Decrement Operators");
        System.out.println(x++);
        System.out.println(x);
        System.out.println(x++);
        //x++ is 'use x and then increment'
        //++x is 'increment x and then use'
    }
}
