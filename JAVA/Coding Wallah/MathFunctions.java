public class MathFunctions {
    public static void main(String[] args) {
        System.out.println("Max"+Math.max(10, 20));// maximum of 10 and 20
        System.out.println("Min"+Math.min(10, 20));// minimum of 10 and 20 
        System.out.println("Abs"+Math.abs(-10));// absolute value of -10 is 10 
        System.out.println("Ceil"+Math.ceil(10.5));// 10.5 is ceiled to 11
        System.out.println("Floor"+Math.floor(10.5)); // 10.5 is floored to 10
        System.out.println("Round"+Math.round(10.5)); // 10.5 is rounded to 11
        System.out.println("Sqrt"+Math.sqrt(10)); // 10^0.5 = 3.1622776601683795
        System.out.println("Pow"+Math.pow(10, 2)); // 10^2 = 10*10 = 100
        System.out.println("PI"+Math.PI); //   pi is a mathematical constant
        System.out.println("E"+Math.E); // e is a mathematical constant 
    }
}
